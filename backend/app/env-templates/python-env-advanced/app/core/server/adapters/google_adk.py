from typing import AsyncIterator, Optional

from .base import (
    BaseSDKAdapter,
    SDKEvent,
    SDKEventType,
    SDKConfig,
    AdapterRegistry,
)

logger = logging.getLogger(__name__)


@AdapterRegistry.register
class GoogleADKAdapter(BaseSDKAdapter):
    """
    Google ADK Wrapper adapter.

    This adapter supports:
    - google-adk-wr/gemini: Google Gemini via ADK
    - google-adk-wr/vertex: Vertex AI via ADK
    - google-adk-wr/openai-compatible: OpenAI-compatible endpoints

    For openai-compatible, configuration is loaded from .google-adk settings files.
    """

    ADAPTER_TYPE = "google-adk-wr"
    SUPPORTED_PROVIDERS = ["gemini", "vertex", "openai-compatible"]

    # Path to settings directory (inside container: /app/core/.google-adk/)
    SETTINGS_DIR = Path("/app/core/.google-adk")

    def __init__(self, config: SDKConfig):
        super().__init__(config)
        logger.info(f"GoogleADKAdapter initialized with provider: {config.provider}")

        # Loaded settings for openai-compatible provider
        self._openai_compatible_settings: Optional[dict] = None

    def _load_settings_for_mode(self, mode: str) -> Optional[dict]:
        """
        Load settings from the .google-adk settings file for the given mode.

        Args:
            mode: Either "building" or "conversation"

        Returns:
            Settings dict if file exists, None otherwise
        """
        settings_file = self.SETTINGS_DIR / f"{mode}_settings.json"
        if settings_file.exists():
            try:
                with open(settings_file, 'r') as f:
                    return json.load(f)
            except Exception as e:
                logger.error(f"Failed to load settings from {settings_file}: {e}")
        return None

    def _get_openai_compatible_config(self, mode: str) -> Optional[dict]:
        """
        Get OpenAI Compatible provider configuration for the given mode.

        Args:
            mode: Either "building" or "conversation"

        Returns:
            Provider config dict with api_key, base_url, model, or None if not configured
        """
        settings = self._load_settings_for_mode(mode)
        if settings:
            providers = settings.get("providers", {})
            return providers.get("openai-compatible")
        return None

    async def send_message_stream(
        self,
        message: str,
        session_id: Optional[str] = None,
        backend_session_id: Optional[str] = None,
        system_prompt: Optional[str] = None,
        mode: str = "conversation",
    ) -> AsyncIterator[SDKEvent]:
        """
        Send message via Google ADK and stream responses.

        For openai-compatible provider:
        1. Load settings from .google-adk/{mode}_settings.json
        2. Initialize OpenAI-compatible client with base_url and api_key
        3. Send message and stream responses
        4. Convert responses to SDKEvent format

        For gemini/vertex providers:
        TODO: Implement full Google ADK integration

        Expected ADK message types to handle:
        - TextPart: Text responses -> SDKEventType.ASSISTANT
        - FunctionCall: Tool invocations -> SDKEventType.TOOL_USE
        - FunctionResponse: Tool results -> SDKEventType.TOOL_RESULT
        - ThoughtPart: Reasoning/thinking -> SDKEventType.THINKING
        """

        # Handle openai-compatible provider
        if self.config.provider == "openai-compatible":
            # Load configuration from settings file
            openai_config = self._get_openai_compatible_config(mode)
            if not openai_config:
                yield SDKEvent(
                    type=SDKEventType.ERROR,
                    content=(
                        f"OpenAI Compatible provider not configured for {mode} mode. "
                        f"Settings file not found at {self.SETTINGS_DIR}/{mode}_settings.json"
                    ),
                    error_type="ConfigurationError",
                    metadata={
                        "adapter_type": self.ADAPTER_TYPE,
                        "provider": self.config.provider,
                        "mode": mode,
                    },
                )
                return

            # Log that we have config (for debugging)
            logger.info(
                f"OpenAI Compatible config loaded for {mode} mode: "
                f"base_url={openai_config.get('base_url')}, model={openai_config.get('model')}"
            )

            # TODO: Implement actual OpenAI-compatible client integration
            # This is where you would:
            # 1. Initialize OpenAI client with openai_config['base_url'] and openai_config['api_key']
            # 2. Create chat completion request with openai_config['model']
            # 3. Stream responses and convert to SDKEvent format

            yield SDKEvent(
                type=SDKEventType.ERROR,
                content=(
                    f"OpenAI Compatible adapter is configured but not yet fully implemented. "
                    f"Configuration loaded: base_url={openai_config.get('base_url')}, "
                    f"model={openai_config.get('model')}. "
                    "Full implementation coming soon."
                ),
                error_type="NotImplementedError",
                metadata={
                    "adapter_type": self.ADAPTER_TYPE,
                    "provider": self.config.provider,
                    "mode": mode,
                    "base_url": openai_config.get("base_url"),
                    "model": openai_config.get("model"),
                },
            )
            return

        # Handle gemini/vertex providers (not yet implemented)
        logger.warning(f"GoogleADKAdapter.send_message_stream called for provider {self.config.provider} but not implemented")

        yield SDKEvent(
            type=SDKEventType.ERROR,
            content=(
                f"Google ADK adapter ({self.config.adapter_id}) is not yet implemented. "
                "This is a placeholder for future Google ADK integration. "
                "Please use a claude-code/* adapter for now."
            ),
            error_type="NotImplementedError",
            metadata={
                "adapter_type": self.ADAPTER_TYPE,
                "provider": self.config.provider,
                "mode": mode,
            },
        )

